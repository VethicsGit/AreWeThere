package com.nanny.AreWeThere;

public class ItemGetterSetter {

    private String Name;

    public ItemGetterSetter() {
    }

    public ItemGetterSetter(String name) {
        Name = name;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
